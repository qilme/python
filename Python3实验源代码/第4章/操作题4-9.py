d={'0':'h','1':'n','2':'b','3':'x','4':'m',	#将密码本定义为字典
   '5':'a','6':'q','7':'f','8':'e','9':'y'}
a=input("请输入一组整数（空格分隔）：")
b=a.split(' ')                              		#将输入的数据转换为列表
rs=[]
for c in b:   								#遍历列表，转换每个数 
   e=''
   for f in c:								#转换每个数中的数字
      e+=d[f]
   rs.append(e)								#将转换结果存入列表
print('加密后：',' '.join(rs))					#输出转换结果
