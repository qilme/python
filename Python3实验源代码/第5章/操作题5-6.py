#用列表表示矩阵
d=[[1,2,3,4,5],[10,9,8,7,6],[11,12,13,14,15]
   ,[20,19,18,17,16],[21,22,23,24,25]]
s=0
for a in d:	#遍历列表，计算所有元素之和
   for b in a:
      s+=b
print('所有元素值和 =',s)
sz=sc=0
for i in range(5):
   sz+=d[i][i]	#计算主对角线元素之和   
   sc+=d[i][4-i]#计算次对角线元素之和
print('主对角线元素之和 =',sz)
print('次对角线元素之和 =',sc)
